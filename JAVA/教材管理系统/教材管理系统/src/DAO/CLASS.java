package DAO;

public class CLASS {
    private int ID;
    private String XName;
    private String ClassName;
    private int num;
    private int bnum;

    public String getXName() {
        return XName;
    }

    public void setXName(String XName) {
        this.XName = XName;
    }

    public int getID() {
        return ID;
    }

    public void setID(int ID) {
        this.ID = ID;
    }

    public String getClassName() {
        return ClassName;
    }

    public void setClassName(String className) {
        ClassName = className;
    }

    public int getNum() {
        return num;
    }

    public void setNum(int num) {
        this.num = num;
    }

    public int getBnum() {
        return bnum;
    }

    public void setBnum(int bnum) {
        this.bnum = bnum;
    }
}
